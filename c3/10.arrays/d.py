#copy() method
arr1=['2', '5', '8', '95']
arr2=arr1.copy()
print(arr2)
print('\n')

#count() method
list1=['red', 'green', 'blue', 'orange', 'green', 'gray', 'green']
cnt=list1.count('green')
print(cnt)
print('\n')

#index() method
fruits=['banana', 'apple', 'cherry']
x=fruits.index('cherry')
print(x)
print('\n')

#insert() method. add element at 1 index
fruits=['apple', 'banana', 'cherry']
fruits.insert(1, 'orange')
print(fruits)
print('\n')

#reverse() method
fruits=['apple', 'banana', 'cherry']
fruits.reverse()
print(fruits)
print('\n')

#sort() method
fruits=['apple', 'cherry', 'banana']
fruits.sort()
print(fruits)